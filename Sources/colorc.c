#include "fdf.h"

void		colorc(t_mlx *mlx)
{
	if (mlx->pcolor == 0)
	{
		mlx->xs = 0xE6E6FA;
		mlx->ys = 0x8B008B;
		return ;
	}
	mlx->xs = 0xADD8E6;
	mlx->ys = 0x191970;
}
